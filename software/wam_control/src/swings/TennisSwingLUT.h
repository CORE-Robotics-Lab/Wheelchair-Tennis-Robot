/***
This file contains a pair of functions to get the cartesian position and quaternion angle of the end of the WAM arm
for different tennis swing motions recorded in TennisLUT.

The two functions at the bottom give you the position and quaternion angle of the end of the WAM arm at any time. If
you ask for a time between two recorded points they interpolate. They return the first point if the time is before
the timestamp of the first recorded point and the last point if the timestamp is after the end of the last recorded point.

TennisLUT is a 3-D array. The first dimension specifies which swing motion to use.
Each row of the LUT is in the format
{time, x position, y position, z position, quaternion w, quaternion x, quaternion y, quaternion z};
For example, TennisLUT[0][2][0] is the timestamp of row 3 of motion 1.
We do not require that the quaternions in each row are normalized; that is done automatically by the acessor functions

We require that time is nondecreasing as you go down a specific recorded motion, but a fixed sampling interval is not required.
We require that each recorded motion is the same length, TennisLUTLength. Since we do not require a fixed sampling interval you
can easily pad motions to the correct length by duplicating the last entry. For example, having duplicate rows like
    {14.746, -0.506195, 0.430475, -0.553168, 0.299203, -0.609854, 0.690922, 0.247349},
    {14.766, -0.506193, 0.430487, -0.553161, 0.299172, -0.609907, 0.690882, 0.247369},
is perfectly valid.


Modified from https://github.com/RoboJackets/roboracing-firmware/blob/EVGP_Spring_2021/rigatoni/Brake/BrakeLUT.h
*/
